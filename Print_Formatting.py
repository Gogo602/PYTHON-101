# Different ways of formatting Strings in Python

# using format
name = "Gogo"
print("Hello {}, welcome to Python 101".format(name))

# using f, which is pretty the modern way
print(f"Hello {name}, welcome to Python 101")

# older way, using %s
print("Hello %s, welcome to Python 101" % name)

# all the above way for formatting strings works but modern apprach is recommended