# Multiple comparison in Python. <and> <or>


# using AND, both conditions must be True inorder to get a True
age = 11
name = "Kalob"

if age >= 18 and name == "Kalob":
    print("I have decisons of my own")
else:
    print("You dont have right")


# using OR, one of the conditions must be True to get a True
if age >= 18 or name == "Kalob":
    print("I have decisons of my own")
else:
    print("You dont have right")